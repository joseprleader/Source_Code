# Make a small program that gets two numbers from the user and executes all
# arithmetic operations covered with those numbers. Print each result to the
# console.

num1 = int(input("Enter a number: "))
num2 = int(input("Enter a number: "))

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 /  num2)
print(num1 ** num2)
print(num1 % num2)
