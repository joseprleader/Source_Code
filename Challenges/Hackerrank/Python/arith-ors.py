# Solution to Arithmetic Operations from Hackerrank
if __name__ == '__main__':
    
    # Get a number from STDIN
    a = int(input())

    # Get another number from STDIN
    b = int(input())
    
    # Sum both numbers
    print(a + b)

    # Subtract both numbers
    print(a - b)

    # Multiply both numbers
    print(a * b)